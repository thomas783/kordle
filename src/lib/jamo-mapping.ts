/**
 * 한글 자모 분해 매핑 테이블
 *
 * 표준 한글 분해(초성/중성/종성)보다 한 단계 더 깊이 분해한다.
 * 게임 키보드에는 단일 자모 24개(자음 14 + 모음 10)만 노출되며,
 * 이외의 모든 자모(쌍자음, 복모음, 겹받침)는 단일 자모 시퀀스로 환원된다.
 *
 * 예) 꽉 → ㄲ + ㅘ + ㄱ → (분해) → ㄱ ㄱ ㅗ ㅏ ㄱ (5자모)
 *
 * ⚠️  매핑 정확도 = 게임 채점 정확도. 검토 필수.
 */

